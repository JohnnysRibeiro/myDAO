public class pessoa {

	private int nome;
	private String cpf;
	private int fone;

	public pessoa() {

	}

	public int getNome () {
		return this.nome;
	}

	public String getCpf () {
		return this.cpf;
	}

	public int getFone () {
		return this.fone;
	}

	public void setNome (int nome) {
		this.nome = nome;
	}

	public void setCpf (String cpf) {
		this.cpf = cpf;
	}

	public void setFone (int fone) {
		this.fone = fone;
	}

}